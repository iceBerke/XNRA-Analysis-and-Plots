def parse_element_spectrum(filepath: str, keep_isotopes: bool = True) -> Dict:
    """
    Parse SIMNRA element spectrum export file.
    
    This file is generated by SIMNRA via:
    Plot → Show Element Spectra (enable)
    File → Write Spectrum Data
    
    Format: Tab-separated columns with header row containing element names.
    First column is channel number, subsequent columns are element contributions.
    
    Parameters
    ----------
    filepath : str
        Path to the element spectrum .txt file
    keep_isotopes : bool, default=True
        If True, preserve isotope-specific spectra separately.
        If False, merge isotopes into parent elements (v8 behavior).
    
    Returns
    -------
    dict
        Dictionary containing:
        - channels: np.array of channel numbers
        - elements: list of element names (from header)
        - isotopes: list of isotope names (e.g., '12C', '16O')
        - element_spectra: dict mapping element name → np.array of counts
        - isotope_spectra: dict mapping isotope name → np.array of counts
        - total_simulated: np.array of summed simulated counts
        - isotope_to_element: dict mapping isotope → parent element
    """
    with open(filepath, 'r') as f:
        lines = f.readlines()
    
    if not lines:
        raise ValueError(f"Empty file: {filepath}")
    
    # Parse header line
    header = lines[0].strip().split('\t')
    
    # First column is 'channel', rest are element/isotope names
    if header[0].lower() != 'channel':
        raise ValueError(f"Expected 'channel' as first column, got: {header[0]}")
    
    column_names = header[1:]
    
    # Categorize columns as elements vs isotopes
    elements = []  # Pure element symbols (no mass number)
    isotopes = []  # Isotopes with mass numbers (e.g., '12C', '16O')
    isotope_to_element = {}  # Map isotope → parent element
    element_column_indices = {}  # element → list of column indices
    isotope_column_indices = {}  # isotope → column index
    
    for i, name in enumerate(column_names):
        if not name:
            continue
        
        # Check if it's an isotope (starts with number) or element (letter only)
        if name[0].isalpha():
            # This is an element total (e.g., 'C', 'O', 'Si')
            elements.append(name)
            element_column_indices[name] = i
        else:
            # This is an isotope (e.g., '12C', '16O')
            isotopes.append(name)
            isotope_column_indices[name] = i
            
            # Find parent element
            parent = ''.join(c for c in name if c.isalpha())
            isotope_to_element[name] = parent
    
    # Parse data lines
    channels = []
    data_columns = {name: [] for name in column_names}
    
    for line in lines[1:]:
        line = line.strip()
        if not line:
            continue
        
        parts = line.split('\t')
        if len(parts) < 2:
            continue
        
        try:
            channel = int(parts[0])
            channels.append(channel)
            
            for i, name in enumerate(column_names):
                if i + 1 < len(parts):
                    # Parse scientific notation (e.g., 1.23E+0004)
                    val = float(parts[i + 1])
                else:
                    val = 0.0
                data_columns[name].append(val)
        except (ValueError, IndexError):
            continue
    
    channels = np.array(channels)
    
    # Convert to numpy arrays - separate element and isotope spectra
    element_spectra = {}
    isotope_spectra = {}
    
    for name in column_names:
        arr = np.array(data_columns[name])
        if name in element_column_indices:
            element_spectra[name] = arr
        elif name in isotope_column_indices:
            isotope_spectra[name] = arr
    
    # Calculate total simulated (sum of element contributions only, to avoid double counting)
    total_simulated = np.zeros(len(channels))
    for elem in elements:
        if elem in element_spectra:
            total_simulated += element_spectra[elem]
    
    return {
        'filepath': str(filepath),
        'channels': channels,
        'elements': elements,
        'isotopes': isotopes,
        'all_columns': column_names,
        'element_spectra': element_spectra,
        'isotope_spectra': isotope_spectra,
        'total_simulated': total_simulated,
        'isotope_to_element': isotope_to_element,
        'element_column_indices': element_column_indices,
        'isotope_column_indices': isotope_column_indices,
    }


