def main():
    """Simple CLI for testing."""
    import argparse
    
    parser = argparse.ArgumentParser(description='Plot SIMNRA element spectra')
    parser.add_argument('input', help='Element spectrum .txt file')
    parser.add_argument('-e', '--elements', help='Comma-separated elements to plot')
    parser.add_argument('-i', '--isotopes', help='Comma-separated isotopes to plot')
    parser.add_argument('--no-total', action='store_true', help='Hide total spectrum')
    parser.add_argument('--energy', action='store_true', help='Use energy x-axis')
    parser.add_argument('--offset', type=float, default=0, help='Calibration offset')
    parser.add_argument('--gain', type=float, default=1, help='Calibration gain')
    parser.add_argument('-t', '--title', help='Plot title')
    
    args = parser.parse_args()
    
    # Parse file
    data = parse_element_spectrum(args.input)
    summary = get_element_spectrum_summary(data)
    
    print(f"\nLoaded: {args.input}")
    print(f"Channels: {summary['channel_min']} - {summary['channel_max']} ({summary['n_channels']} points)")
    print(f"Elements: {', '.join(summary['elements'])}")
    print(f"Isotopes: {', '.join(summary['isotopes'])}")
    
    # Determine what to plot
    elements = None
    isotopes = None
    
    if args.elements:
        elements = [e.strip() for e in args.elements.split(',')]
    if args.isotopes:
        isotopes = [i.strip() for i in args.isotopes.split(',')]
    
    # Plot
    plot_element_spectra(
        data,
        elements_to_plot=elements,
        isotopes_to_plot=isotopes,
        show_total=not args.no_total,
        x_axis_mode='energy' if args.energy else 'channel',
        cal_offset=args.offset,
        cal_gain=args.gain,
        title=args.title,
    )


if __name__ == '__main__':
    main()
