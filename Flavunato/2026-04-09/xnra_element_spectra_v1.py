def plot_element_spectra(element_spectrum_data: Dict,
                         elements_to_plot: List[str] = None,
                         isotopes_to_plot: List[str] = None,
                         show_total: bool = True,
                         figure_size: Tuple[float, float] = (12, 6),
                         dpi: int = 100,
                         title: str = None,
                         x_axis_mode: str = 'channel',
                         cal_offset: float = 0,
                         cal_gain: float = 1,
                         line_width: float = 1.5,
                         show_legend: bool = True,
                         show_plot: bool = True):
    """
    Plot element and/or isotope spectra from SIMNRA element spectrum file.
    
    Features interactive hover to identify elements/isotopes (requires mplcursors).
    
    Parameters
    ----------
    element_spectrum_data : dict
        Output from parse_element_spectrum()
    elements_to_plot : list, optional
        List of element symbols to plot. If None, plots all.
    isotopes_to_plot : list, optional
        List of isotope names to plot. If None and elements_to_plot is None, plots none.
    show_total : bool, default=True
        Show the total simulated spectrum as a black dashed line
    figure_size : tuple
        Figure size in inches
    dpi : int
        Figure resolution
    title : str, optional
        Plot title
    x_axis_mode : str
        'channel' or 'energy'
    cal_offset, cal_gain : float
        Energy calibration parameters (E = offset + gain × channel)
    line_width : float
        Line width for spectra
    show_legend : bool
        Show legend
    show_plot : bool
        Display the plot (call plt.show())
    
    Returns
    -------
    matplotlib.figure.Figure
        The figure object
    """
    import matplotlib.pyplot as plt
    
    channels = element_spectrum_data['channels']
    element_spectra = element_spectrum_data.get('element_spectra', {})
    isotope_spectra = element_spectrum_data.get('isotope_spectra', {})
    total_simulated = element_spectrum_data.get('total_simulated', None)
    
    # Determine what to plot
    if elements_to_plot is None and isotopes_to_plot is None:
        # Default: plot all elements
        elements_to_plot = element_spectrum_data.get('elements', [])
    
    if elements_to_plot is None:
        elements_to_plot = []
    if isotopes_to_plot is None:
        isotopes_to_plot = []
    
    # Create figure
    fig, ax = plt.subplots(figsize=figure_size, dpi=dpi)
    
    # X-axis values
    if x_axis_mode.lower() == 'energy':
        x_values = cal_offset + cal_gain * channels
        x_label = 'Energy [keV]'
    else:
        x_values = channels
        x_label = 'Channel'
    
    color_idx = 0
    lines = []  # Store line objects for hover
    line_labels = []  # Store corresponding labels
    
    # Plot total first (as background reference)
    if show_total and total_simulated is not None:
        line, = ax.plot(x_values, total_simulated, 
                color='black', linestyle='--', linewidth=line_width * 0.8,
                label='Total', alpha=0.5, zorder=1)
        lines.append(line)
        line_labels.append('Total (simulated)')
    
    # Plot elements
    for elem in elements_to_plot:
        if elem in element_spectra:
            color = ELEMENT_COLORS[color_idx % len(ELEMENT_COLORS)]
            line, = ax.plot(x_values, element_spectra[elem],
                    color=color, linewidth=line_width,
                    label=elem, zorder=2)
            lines.append(line)
            line_labels.append(elem)
            color_idx += 1
    
    # Plot isotopes
    for iso in isotopes_to_plot:
        if iso in isotope_spectra:
            color = ELEMENT_COLORS[color_idx % len(ELEMENT_COLORS)]
            line, = ax.plot(x_values, isotope_spectra[iso],
                    color=color, linewidth=line_width,
                    label=iso, linestyle='-.', zorder=2)
            lines.append(line)
            line_labels.append(iso)
            color_idx += 1
    
    # Labels and title
    ax.set_xlabel(x_label, fontsize=12, fontweight='bold')
    ax.set_ylabel('Counts', fontsize=12, fontweight='bold')
    
    if title:
        ax.set_title(title, fontsize=14, fontweight='bold')
    else:
        ax.set_title('Element Spectra', fontsize=14, fontweight='bold')
    
    # Legend
    if show_legend and (elements_to_plot or isotopes_to_plot or show_total):
        ax.legend(loc='upper right', fontsize=9, framealpha=0.9)
    
    # Grid
    ax.grid(True, alpha=0.3, linestyle='--')
    
    # ===== Interactive Hover using mplcursors =====
    try:
        import mplcursors
        
        # Create cursor for all lines
        # hover=2 makes annotation disappear immediately when not on a line
        cursor = mplcursors.cursor(lines, hover=2)
        
        @cursor.connect("add")
        def on_add(sel):
            """Customize the annotation when hovering"""
            line = sel.artist
            if line in lines:
                idx = lines.index(line)
                label = line_labels[idx]
                color = line.get_color()
                
                # Get position info
                x_val = sel.target[0]
                y_val = sel.target[1]
                
                # Build annotation text
                if label == 'Total (simulated)':
                    # Total spectrum: black, no color name
                    if x_axis_mode.lower() == 'energy':
                        text = f"Total\n{x_val:.1f} keV | {y_val:.0f} cts"
                    else:
                        text = f"Total\nCh {x_val:.0f} | {y_val:.0f} cts"
                    text_color = 'black'
                    border_color = 'black'
                else:
                    # Element/isotope: colored with color name
                    color_name = COLOR_NAMES.get(color, color)
                    if x_axis_mode.lower() == 'energy':
                        text = f"{label} ({color_name})\n{x_val:.1f} keV | {y_val:.0f} cts"
                    else:
                        text = f"{label} ({color_name})\nCh {x_val:.0f} | {y_val:.0f} cts"
                    text_color = color
                    border_color = color
                
                # Style the annotation
                sel.annotation.set_text(text)
                sel.annotation.set_fontsize(10)
                sel.annotation.set_fontweight('bold')
                sel.annotation.set_color(text_color)
                sel.annotation.get_bbox_patch().set_facecolor('white')
                sel.annotation.get_bbox_patch().set_alpha(0.95)
                sel.annotation.get_bbox_patch().set_edgecolor(border_color)
                sel.annotation.get_bbox_patch().set_linewidth(2)
        
        print("✓ Interactive hover enabled (mplcursors)")
        
    except ImportError:
        print("ℹ️  For interactive hover, install mplcursors: pip install mplcursors")
    
    plt.tight_layout()
    
    if show_plot:
        plt.show()
    
    return fig


